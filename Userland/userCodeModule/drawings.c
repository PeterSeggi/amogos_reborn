void draw_snake(uint16_t bitmap[][dibHeight], uint16_t layers, uint32_t colors[], uint64_t init_x, uint64_t init_y){
    for(int i=0; i<layers; i++){
        draw(bitmap[i], colors[i], dibHeight, init_x, init_y);
    }
}
